                                <div class="panel-body">
                                    <p><strong>innodb_buffer_pool_size</strong>: this is the #1 setting to look at for
                                        any installation using InnoDB. The buffer pool is where data and indexes are
                                        cached: having it as large as possible will ensure you use memory and not disks
                                        for most read operations.</p>
                                    <p><strong>innodb_log_file_size:</strong> this is the size of the redo logs. The
                                        redo logs are used to make sure writes are fast and durable and also during
                                        crash recovery.</p>
                                    <p><strong>max_connections:</strong> if you are often facing the &lsquo;Too many
                                        connections&rsquo; error, max_connections is too low. It is very frequent that
                                        because the application does not close connections to the database correctly,
                                        you need much more than the default 151 connections.</p>
                                    <p><strong>innodb_file_per_table:</strong> this setting will tell InnoDB if it
                                        should store data and indexes in the shared tablespace (innodb_file_per_table =
                                        OFF) or in a separate .ibd file for each table (innodb_file_per_table= ON).
                                        Having a file per table allows you to reclaim space when dropping, truncating or
                                        rebuilding a table.</p>
                                    <p><strong>innodb_flush_log_at_trx_commit:</strong> the default setting of 1 means
                                        that InnoDB is fully ACID compliant. It is the best value when your primary
                                        concern is data safety, for instance on a master. However, it can have a
                                        significant overhead on systems with slow disks because of the extra fsyncs that
                                        are needed to flush each change to the redo logs.</p>
                                    <p><strong>innodb_flush_method:</strong> this setting controls how data and logs are
                                        flushed to disk. Popular values are O_DIRECT when you have a hardware RAID
                                        controller with a battery-protected write-back cache and fdatasync (default
                                        value) for most other scenarios. sysbench is a good tool to help you choose
                                        between the 2 values.</p>
                                    <p><strong>innodb_log_buffer_size:</strong> this is the size of the buffer for
                                        transactions that have not been committed yet. The default value (1MB) is
                                        usually fine but as soon as you have transactions with large blob/text fields,
                                        the buffer can fill up very quickly and trigger extra I/O load. Look at the
                                        Innodb_log_waits status variable and if it is not 0,
                                        increase&nbsp;innodb_log_buffer_size</p>
                                    <p><strong>log_bin:</strong> enabling binary logging is mandatory if you want the
                                        server to act as a replication master. If so, don&rsquo;t forget to also set
                                        server_id to a unique value. It is also useful for a single server when you want
                                        to be able to do point-in-time recovery: restore your latest backup and apply
                                        the binary logs.</p>
                                    <p><strong>key_buffer_size</strong> &ndash; Very important if you use MyISAM tables.
                                        Set up to 30-40% of available memory if you use MyISAM tables exclusively. Right
                                        size depends on amount of indexes, data size and workload &ndash; remember
                                        MyISAM uses OS cache to cache the data so you need to leave memory for it as
                                        well, and data can be much larger than indexes in many cases.</p>
                                    <p><strong>table_cache</strong> &ndash; Opening tables can be expensive. For example
                                        MyISAM tables mark MYI header to mark table as currently in use. You do not want
                                        this to happen so frequently and it is typically best to size your cache so it
                                        is large enough to keep most of your tables open. It uses some OS resources and
                                        some memory but for modern hardware it is typically not the problem. 1024 is
                                        good value for applications with couple hundreds tables (remember each
                                        connection needs its own entry) if you have many connections or many tables
                                        increase it larger. I&rsquo;ve seen values over 100.000 used.</p>
                                    <p><strong>thread_cache</strong> &ndash; Thread creation/destructions can be
                                        expensive, which happen at each connect/disconnect. I normally set this value to
                                        at least 16. If application has large jumps in amount of concurrent connections
                                        and I see fast growth of</p>
                                    <p>Threads_Created variable I boost it higher. The goal is not to have threads
                                        created in normal operation.</p>
                                    <p>&nbsp;</p>
                                    <p><strong>query_cache_size</strong> &ndash;&nbsp; If your application is read
                                        intensive and you do not have application level caches this can be great help to
                                        optimize your MySQL database. Do not set it too large as it may slow things down
                                        as its maintenance may get expensive. Values from 32M to 512M normally make
                                        sense. Check it however after a while and see if it is well used. For certain
                                        workloads cache hit ratio is lower than would justify having it enabled.</p>>
