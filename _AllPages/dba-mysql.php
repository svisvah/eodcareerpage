                                    <p>MySQL supports two (or three, depending on how you look at it) different methods
                                        of replicating databases from master to slave. All of these methods use the
                                        binary log; however, they differ in the type of data that is written to the
                                        master&rsquo;s binary log.</p>
                                    <p><strong>Statement-based replication</strong> Under this method, the binary log
                                        stores the SQL statements used to change databases on the master server. The
                                        slave reads this data and re-executes these SQL statements to produce a copy of
                                        the master database. This is the default replication method in MySQL 5.1.11 and
                                        earlier and MySQL 5.1.29 onwards.</p>
                                    <p><strong>Row-based replication</strong> Under this method, the binary log stores
                                        the record-level changes that occur to database tables on the master server. The
                                        slave reads this data and manipulates its records accordingly to produce a copy
                                        of the master database.</p>
                                    <p><strong>Mixed-format replication</strong> Under this method, the server can
                                        dynamically choose between statement-based replication and row-based
                                        replication, depending on certain conditions. Some of these conditions include
                                        using a user-defined function (UDF), using an INSERT command with the DELAYED
                                        clause, using temporary tables, or using a statement that uses system variables.
                                        This is the default replication method in MySQL 5.1.12 to MySQL 5.1.28.</p>
