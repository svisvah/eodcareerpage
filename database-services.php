<script>
    function isValidEmail(email) {
        var emailPattern = /^\S+@\S+\.\S+$/;
        return emailPattern.test(email);
    }

    function submitForm() {
        $.ajax({
            type: 'post',
            url: 'dba-service-form.php',
            data: $('#dba-service-form').serialize() + '&action=submit',
            success: function(response) {
                if (response == "  true") {
                    $('p.msg-tag').addClass('success');
                    $("p.msg-tag").text("Thank you for submitting");
                } else {
                    $('p.msg-tag').addClass('error');
                    $("p.msg-tag").text("Something went Wrong");
                }
                $('#dba-service-form').trigger("reset");

                $('html, body').animate({
                    scrollTop: $(".container.eodformstyle").offset().top
                }, 2000);
            }
        });
    }

    function validateEmail() {
        let existCustomer = $("input#email").val();
        let newCustomer = $('input#new-email').val();
        let fullName = $('input#full-name').val();
        let contactNumber = $('input#contact-number').val();
        let email = '';
        let caction = '';
        if (existCustomer) {
            caction = 'exist';
            email = existCustomer;
        } else {
            if (newCustomer) {
                caction = 'new';
                email = newCustomer;
            }
        }
        $.ajax({
            type: 'post',
            url: 'dba-service-form.php',
            data: 'email=' + email + '&action=email-val&caction=' + caction,
            success: function(response) {
                console.log(caction)
                console.log(response)
                if (caction == 'new' && response == '  false') {
                    $("span#login-new-email").text("Email Address already exist.  Try login");
                }
                if (caction == "exist" && response == "  false") {
                    console.log("test")
                    $("span#login-email").text("No account present with this email address try to login");
                }
                if (response == "  true") {
                    submitForm();
                }
            }
        });
    }

    $("body").delegate("input[name='dba-tech']", "click", function() {
        // Get the value of the selected radio button
        var selectedValue = $(this).val();
        if (selectedValue == 'others') {
            $('input#dba-tech-other').prop("disabled", false);
        } else {
            $('input#dba-tech-other').prop("disabled", true);
        }
    });

    $(document).ready(function() {
        $("body").delegate("#dba-submit", "click", async function() {
            $(".error-message").text("");
            let dbaTech = $("input[name='dba-tech']:checked").val();
            let dbaServices = $("input[name='dba-services']:checked").val();
            let existCustomer = $("input#email").val();
            let newCustomer = $('input#new-email').val();
            let fullName = $('input#full-name').val();
            let contactNumber = $('input#contact-number').val();

            let hasErrors = false; // Flag to track validation errors

            if (!existCustomer && !newCustomer) {
                $('#contact-info').text("Contact information is required");
                hasErrors = true;
            } else {
                if (existCustomer) {
                    if (!isValidEmail(existCustomer)) {
                        $("span#login-email").text("Please Enter a Valid Email Address.");
                        hasErrors = true;
                    }
                } else {
                    if (newCustomer) {
                        if (!isValidEmail(newCustomer)) {
                            $("span#login-new-email").text("Please Enter a Valid Email Address.");
                            hasErrors = true;
                        }
                        if (!fullName) {
                            $("span#login-name").text("Please Enter the Name.");
                            hasErrors = true;
                        }
                        if (!contactNumber) {
                            $("span#login-contact-no").text("Please Enter the Contact Number.");
                            hasErrors = true;
                        }
                    }
                }
            }

            if (!dbaTech) {
                $("#dba-tech-error").text("Please select a Technology.");
                hasErrors = true;
            }

            if (dbaTech == 'others') {
                let otherTech = $('input#dba-tech-other').val();
                if (!otherTech) {
                    $("#dba-tech-error").text("Please specify the Technology Name.");
                    hasErrors = true;
                }
            }

            if (!dbaServices) {
                $("#dba-services-error").text("Please select a Service Type.");
                hasErrors = true;
            }
            if (!hasErrors) {
                validateEmail();
            }
        });

    });
</script>
